def columna(d, codUn, col, f):
    lista = list(
        map(lambda ind: f(d[codUn][ind][col]), range(len(d[codUn]))))
    return lista


def informe(d: dict, codUn: str, codRes: int):
    lista = []
    if codRes == 1:
        temp = columna(d, codUn, "Salario", int)
        s = temp.index(min(temp))
        lista.append(d[codUn][s]["IdEmpl"])
        temp2 = columna(d, codUn, "Bonificacion", int)
        s2 = temp2.index(max(temp2))
        lista.append(d[codUn][s2]["IdEmpl"])
        lista.append((d[codUn][s]["Empleo"], d[codUn][s]
                     ["Departamento"], d[codUn][s]["Seccion"]))
        lista.append((d[codUn][s2]["Empleo"], d[codUn][s2]
                     ["Departamento"], d[codUn][s2]["Seccion"]))
        lista = tuple(lista)
    elif codRes == 2:
        temp = columna(d, codUn, "Salario", int)
        s = temp.index(max(temp))
        temp2 = columna(d, codUn, "Bonificacion", int)
        s2 = temp2.index(min(temp2))
        lista.append((d[codUn][s]["IdEmpl"], d[codUn][s2]["IdEmpl"]))
        lista.append((d[codUn][s]["Empleo"], d[codUn][s]
                     ["Departamento"], d[codUn][s]["Seccion"]))
        lista.append((d[codUn][s2]["Empleo"], d[codUn][s2]
                     ["Departamento"], d[codUn][s2]["Seccion"]))

    elif codRes == 3:
        lista = []
        listado = []
        listado2 = []
        listado3 = []
        listado4 = []
        for x in list(d.keys()):
            if x != codUn:
                temp = columna(d, x, "Salario", int)
                s = temp.index(min(temp))
                listado.append((d[x][s]["IdEmpl"], d[x][s]["Empleo"],
                               d[x][s]["Departamento"], d[x][s]["Seccion"]))
                listado2.append(int(d[x][s]["Salario"]))

                temp2 = columna(d, x, "Bonificacion", int)
                s2 = temp2.index(max(temp2))
                listado3.append(
                    (d[x][s2]["IdEmpl"], d[x][s2]["Empleo"], d[x][s2]["Departamento"], d[x][s2]["Seccion"]))
                listado4.append(int(d[x][s2]["Bonificacion"]))
        lista.append(listado[listado2.index(min(listado2))])
        lista.append(listado3[listado4.index(max(listado4))])

    return lista
