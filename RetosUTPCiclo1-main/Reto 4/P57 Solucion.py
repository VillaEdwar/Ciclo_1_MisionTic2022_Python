def columna(d, anio, col, f):
    lista = list(
        map(lambda ind: f(d[anio][ind][col]), range(len(d[anio]))))
    return lista

#print(columna(d, "2013", "Cont1", int))
def analisis(d: dict, anio: str, codRes: int):
    lista = []
    if codRes == 1:
        temp = columna(d, anio, "Cont1", int)
        s = temp.index(min(temp))
        lista.append(d[anio][s]["CodCiud"])
        temp2 = columna(d, anio, "Cont2", int)
        s2 = temp2.index(max(temp2))
        lista.append(d[anio][s2]["CodCiud"])
        lista.append((d[anio][s]["PunMues"], d[anio][s]
                     ["Localidad"], d[anio][s]["Mes"]))
        lista.append((d[anio][s2]["PunMues"], d[anio][s2]
                     ["Localidad"], d[anio][s2]["Mes"]))
        lista = tuple(lista)
    elif codRes == 2:
        temp = columna(d, anio, "Cont1", int)
        s = temp.index(max(temp))
        temp2 = columna(d, anio, "Cont2", int)
        s2 = temp2.index(min(temp2))
        lista.append((d[anio][s]["CodCiud"], d[anio][s2]["CodCiud"]))
        lista.append((d[anio][s]["PunMues"], d[anio][s]
                     ["Localidad"], d[anio][s]["Mes"]))
        lista.append((d[anio][s2]["PunMues"], d[anio][s2]
                     ["Localidad"], d[anio][s2]["Mes"]))

    elif codRes == 3:
        lista = []
        listado = []
        listado2 = []
        listado3 = []
        listado4 = []
        for x in list(d.keys()):
            if x != anio:
                temp = columna(d, x, "Cont1", int)
                s = temp.index(min(temp))
                listado.append((d[x][s]["CodCiud"], d[x][s]["PunMues"],
                               d[x][s]["Localidad"], d[x][s]["Mes"]))
                listado2.append(int(d[x][s]["Cont1"]))

                temp2 = columna(d, x, "Cont2", int)
                s2 = temp2.index(max(temp2))
                listado3.append(
                    (d[x][s2]["CodCiud"], d[x][s2]["PunMues"], d[x][s2]["Localidad"], d[x][s2]["Mes"]))
                listado4.append(int(d[x][s2]["Cont2"]))
        lista.append(listado[listado2.index(min(listado2))])
        lista.append(listado3[listado4.index(max(listado4))])

    return lista
